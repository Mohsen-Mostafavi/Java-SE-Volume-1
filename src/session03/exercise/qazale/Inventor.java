package session03.exercise.qazale;

public class Inventor {
    String name;
    String lastName;
    String nationalCode;

    public Inventor(String name, String lastName, String nationalCode) {
        this.name = name;
        this.lastName = lastName;
        this.nationalCode = nationalCode;
    }

    public Inventor(String name, String lastName) {
        this.name = name;
        this.lastName = lastName;
    }

}
